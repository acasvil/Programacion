package yoquese;

public class Carta {
	
	private String color;
	private String number;
	
	public Carta(String color, String number) {
		super();
		this.color = color;
		this.number = number;
	}
	
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	
	
	
	
}
